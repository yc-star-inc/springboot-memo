package com.tsmc.ecp.preq.vo;

public class PreqConfigObject {

    private String enableSpc;
    private String enableDefect;
    private String enableFac;

    public String getEnableSpc() {
        return enableSpc;
    }

    public void setEnableSpc(String enableSpc) {
        this.enableSpc = enableSpc;
    }

    public String getEnableDefect() {
        return enableDefect;
    }

    public void setEnableDefect(String enableDefect) {
        this.enableDefect = enableDefect;
    }

    public String getEnableFac() {
        return enableFac;
    }

    public void setEnableFac(String enableFac) {
        this.enableFac = enableFac;
    }
}
